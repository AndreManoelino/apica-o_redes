# Coordenada do botão "Adicionar" e tempo de espera
botao_adicionar = (670, 529)
tempo_espera = 4  # Espera de 4 segundos após clicar no botão "Adicionar"
botao_descer_planilha = (2746,321)
def copia_e_colar_por_linha(primeira_parte, rotas_fixas_interface, botao_adicionar, tempo_espera,botao_descer_planilha):
    for i in range(0, len(primeira_parte), len(rotas_fixas_interface)):
        for j, (coord_planilha, coord_interface) in enumerate(zip(primeira_parte[i:i+len(rotas_fixas_interface)], rotas_fixas_interface)):
            # Vai até a célula específica na planilha e copia
            pyautogui.click(coord_planilha, duration=0.1)
            pyautogui.hotkey('ctrl', 'c')
            sleep(1)
            
            # Cola no campo da interface correspondente
            pyautogui.click(coord_interface, duration=0.2)
            pyautogui.hotkey('ctrl', 'v')
            sleep(1)
        
        # Clica no botão "Adicionar" após preencher os campos
        pyautogui.click(botao_adicionar, duration=2)
        sleep(tempo_espera)  # Espera 4 segundos variavel adicionada acima 
    if primeira_parte== botao_adicionar:
        return botao_descer_planilha
    
